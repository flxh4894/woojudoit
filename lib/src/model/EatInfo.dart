class EatInfo {

  String title;
  int totalKcal;
  String food;
  String category;
  String ingredient;
  int gram;
  int kcal;
  bool favorite;

  EatInfo({
    required this.title,
    required this.totalKcal,
    required this.food,
    required this.category,
    required this.ingredient,
    required this.gram,
    required this.kcal,
    required this.favorite
  });

}